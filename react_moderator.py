def post_message_tool(recipient_id: str, message_json: str) -> str:
    """
    ÊåáÂÆö„Åó„Åü„Ç®„Éº„Ç∏„Çß„É≥„Éà„Å´„É°„ÉÉ„Çª„Éº„Ç∏„ÇíÈÄÅ‰ø°„Åó„Åæ„Åô„ÄÇ

    Args:
        recipient_id: ÈÄÅ‰ø°ÂÖà„ÅÆ„Ç®„Éº„Ç∏„Çß„É≥„ÉàID (‰æã: 'DEBATER_A', 'JUDGE_L')
        message_json: ÈÄÅ‰ø°„Åô„Çã„É°„ÉÉ„Çª„Éº„Ç∏„ÅÆJSONÊñáÂ≠óÂàó

    Returns:
        ÈÄÅ‰ø°ÁµêÊûú„ÅÆ„É°„ÉÉ„Çª„Éº„Ç∏
    """
    try:
        message_body = json.loads(message_json)
        message_broker.post_message(recipient_id, message_body)
        return f"Message successfully sent to {recipient_id}."
    except json.JSONDecodeError as e:
        return f"Error: Invalid JSON format - {e}"
    except Exception as e:
        return f"Error: Failed to send message - {e}"


def get_my_message_tool(my_id: str) -> str:
    """
    Ëá™ÂàÜÂÆõ„ÅÆÊú™Ë™≠„É°„ÉÉ„Çª„Éº„Ç∏„ÇíÂèñÂæó„Åó„Åæ„Åô„ÄÇ

    Args:
        my_id: Ëá™ÂàÜ„ÅÆ„Ç®„Éº„Ç∏„Çß„É≥„ÉàID

    Returns:
        „É°„ÉÉ„Çª„Éº„Ç∏„ÅÆJSONÊñáÂ≠óÂàó„ÄÅ„Åæ„Åü„ÅØ "No new messages."
    """
